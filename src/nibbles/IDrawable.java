package nibbles;

/**
 * An interface for all objects that intend to be drawn to the screen.
 * 
 * @author Steven
 */
public interface IDrawable {
    
    /**
     * This method replaces the god-awful 'Set' subroutine. Override
     * this method to dictate how the GUI draws an object.
     */
    public void draw(boolean colorful);
    
}
